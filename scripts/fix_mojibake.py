content = fix_latin_high(content)
content = fix_latin_mid(content)
for old, new in emoji_fixes:
    content = content.replace(old, new)

if content != original:
    with open('apps/backend/src/modules/quotes/quotes.service.ts', 'wb') as f:
        f.write(content)
    changed = sum(1 for a, b in zip(original, content) if a != b)
    print(f"Fixed! File changed ({len(original)} → {len(content)} bytes)")
else:
    print("No changes made")
